package com.lonely.wolf.note.list;

public class ListNode {
    public int val;
    public ListNode next;

    public int getVal(){
        return this.val;
    }

    ListNode(int x) {
        val = x;
        next = null;
    }

    public static void main(String[] args) {
        System.out.println(15 / 10);
    }
}
